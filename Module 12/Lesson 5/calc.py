def add(n,m):
    return n+m
def subtract(n,m):
    return n-m
def multiply(n,m):
    return n*m
def divide(n,m):
    return n/m

num1=int(input("Enter num1: "))
num2=int(input("Enter num2: "))

print("Addition- ",add(num1,num2))

print("Subtration- ",subtract(num1,num2))

print("Multiplication- ",multiply(num1,num2))

print("Division- ",divide(num1,num2))